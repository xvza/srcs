function  get()                                            {
############################################################
  source    "head.sh"
  source    "core.sh"  "$(command -v ${1})"  "program     :"
  source    "foot.sh"
  source    "${1}.sh"
  declare   "-g"  "${FUNCNAME[1]}=${!FUNCNAME[1]}"
############################################################
                                                           }
############################################################
function  qh()                                             {
############################################################
  declare   "-i"  "n=${2} + 2"
  eval      "get ${1}"
  eval      "${1} ${!FUNCNAME[0]} ${@:3} | head -${n}"
############################################################
                                                           }
