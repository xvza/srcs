function    get()                                          {
############################################################
  source    "head.sh"
  source    "core.sh"  "$(command -v ${1})"  "program     :"
  source    "foot.sh"
  source    "${1}.sh"
  declare   "-g"  "${2}=${!2}"
############################################################
                                                           }
