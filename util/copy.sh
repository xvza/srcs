#!/usr/bin/env bash
#############################################
[[ -z   ${3} ]] &&                          \
declare   "bin=$(cp -vpr ${2?}/* ${1?})"
#############################################
[[ -n   ${3} ]] &&                          \
declare   "bin=$(cp -vpr ${2?}/.??* ${1?})"
#############################################
declare   "str=cp: ${1?} directory is newest"
#############################################
[[ -z ${bin} ]] &&                          \
echo      "${str}"
#############################################
[[ -n ${bin} ]] &&                          \
echo      "${bin}"
