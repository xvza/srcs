#!/usr/bin/env bash
####################################################
declare   "bin=ogg123"
declare   "s0=music directory"
declare   "id=$(pgrep ${bin})"
declare   "state=$(ps -q${id} -ostate --no-headers)"
####################################################
[[ ${1} -eq "1" && ${state} == "T" ]]              \
&& eval   "kill -CONT ${id}"
####################################################
[[ ${1} -eq "1" && ${state} == "S" ]]              \
&& eval   "kill -STOP ${id}"
####################################################
[[ ${1} -eq "0"  ]]                                \
&& eval   "killall -TERM ${bin}"
####################################################
[[ -e ${SKEL}/${1?$s0} ]]                          \
&& eval   "${bin} -z ${SKEL}/${1}"
####################################################
