#!/usr/bin/env bash
##############################################
function  xd()                               {
##############################################
  eval      "xdg-open ${1?}"                 &
##############################################
                                             }
##############################################
function  sr()                               {
##############################################
  local     "s0=unknown search"
  eval      "ls -Ra ${2-./}|grep ${1?}"
##############################################
                                             }
##############################################
function  so()                               {
##############################################
  local     "s0=unknown search"
  eval      "locate ${1?}|grep ${1?$s0}"
##############################################
                                             }
##############################################
function  hs()                               {
##############################################
  local     "s0=unknown search"
  eval      "history|grep ${1?$s0}"
##############################################
                                             }
##############################################
function  rmo()                              {
##############################################
  local     "s0=undefined file"
##############################################
  eval      "${1?$s0}"
##############################################
  [[ -O $(dirname ${1}) ]]                   \
  || eval   "sudo ${BASH_ALIASES[rm]} ${@}"  \
  || return
##############################################
  [[ -O $(dirname ${1}) ]]                   \
  && eval   "${BASH_ALIASES[rm]} ${@}"       \
  && return
##############################################
                                             }
##############################################
function  rt()                               {
##############################################
  cd "${http}/${1?}"                         \
  && [[ ${2} == "g" ]]                       \
  && source "push.sh"
##############################################
                                             }
##############################################
