#!/usr/bin/env bash
###########################################################
echo 	    "use with source builtin"
###########################################################
set -u
###########################################################
declare     "srcs=$(dirname ${BASH_SOURCE[0]})"
declare     "http=${srcs}/.."
declare     "shel=${srcs}/shell.d"
declare     "file=$(find ${shel} -type f)"
