#!/usr/bin/env bash
###########################################################
declare   "dirs=$(dirname ${BASH_SOURCE[0]})"
declare   "l0=directory label"
declare   "l1=directory not found"
###########################################################
source    "dire.sh"  "${dirs}/../.."
source    "shel.sh"
###########################################################
[[ -z ${1?$l0}    ]] && exit
###########################################################
declare   "mesg[0]=${dire[$1]}"
declare   "mesg[1]=${l1}"
###########################################################
[[ -n ${dire[$1]} ]]
###########################################################
declare   "e=${?}"
declare   "c=${shell[$e]}"
###########################################################
echo      "${c}${1}: ${mesg[$e]}${shell[init]}"
###########################################################
