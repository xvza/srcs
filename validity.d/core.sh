#!/usr/bin/env bash
###########################################################
source    "shel.sh"
###########################################################
declare   "col=${shell[3]}"
declare   "msg=${1:-${shell[1]}none}"
###########################################################
[[ -e ${1} ]] && declare "col=${shell[0]}"
[[    ${3} ]] && declare "col=${shell[2]}"
[[ !  ${3} ]] && [[ ! -e  ${1} ]] && declare "err=1"
[[    ${3} ]] && [[   -z  ${1} ]] && declare "err=1"
###########################################################
echo      "${2}  ${col}${msg}${shell[init]}"
